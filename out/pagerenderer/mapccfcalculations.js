async function mapDataIterator(delegate) {
    let ret = new Uint8ClampedArray(WIDTH * HEIGHT * 4);
    let then = Date.now();
    for (let i = 0; i < ret.length / 4; i++) {
        let res = delegate(i * 4);
        ret[i * 4] = res[0];
        ret[i * 4 + 1] = res[1];
        ret[i * 4 + 2] = res[2];
        ret[i * 4 + 3] = res[3];
        if (i % WIDTH == 0) {
            let now = Date.now();
            if (now - then > 100) {
                await reportProgress(i, progressText);
                await new Promise((resolve) => setTimeout(resolve));
                then = now;
            }
        }
    }
    return ret;
}
async function advanceMap(imgArray, formula, options) {
    let newImgArray = new Uint8ClampedArray(imgArray.length);
    let then = Date.now();
    for (let i = 0; i < newImgArray.length; i += 4) {
        let now = Date.now();
        if (now - then > 2000) {
            await new Promise(resolve => setTimeout(resolve));
            reportProgress(i / 4, progressText);
            then = now;
        }
        let newPixel = formula(imgArray, i, options);
        newImgArray[i] = newPixel[0];
        newImgArray[i + 1] = newPixel[1];
        newImgArray[i + 2] = newPixel[2];
        newImgArray[i + 3] = newPixel[3];
    }
    return newImgArray;
}
